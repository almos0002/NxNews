const BS_EPOCH_AD = { year: 1943, month: 3, day: 14 };

function adDaysSinceEpoch(year: number, month: number, day: number): number {
  const d1 = new Date(BS_EPOCH_AD.year, BS_EPOCH_AD.month, BS_EPOCH_AD.day);
  const d2 = new Date(year, month, day);
  return Math.round((d2.getTime() - d1.getTime()) / 86400000);
}

export function adToBS(year: number, month: number, day: number): { year: number; month: number; day: number } {
  let days = adDaysSinceEpoch(year, month, day);
  let bsYear = 2000;
  let bsMonth = 0;
  let bsDay = 1;

  for (let y = 2000; y <= 2090; y++) {
    const months = BS_DATA[y];
    if (!months) break;
    for (let m = 0; m < 12; m++) {
      if (days < months[m]) {
        bsYear = y;
        bsMonth = m;
        bsDay = days + 1;
        return { year: bsYear, month: bsMonth, day: bsDay };
      }
      days -= months[m];
    }
  }
  return { year: bsYear, month: bsMonth, day: bsDay };
}

export function bsToAD(year: number, month: number, day: number): Date {
  let totalDays = day - 1;
  for (let y = 2000; y < year; y++) {
    const months = BS_DATA[y];
    if (!months) break;
    totalDays += months.reduce((a, b) => a + b, 0);
  }
  const months = BS_DATA[year];
  if (months) {
    for (let m = 0; m < month; m++) {
      totalDays += months[m];
    }
  }
  const ref = new Date(BS_EPOCH_AD.year, BS_EPOCH_AD.month, BS_EPOCH_AD.day);
  ref.setDate(ref.getDate() + totalDays);
  return ref;
}

export function getDaysInBSMonth(year: number, month: number): number {
  return BS_DATA[year]?.[month] ?? 30;
}

export function getFirstWeekdayOfBSMonth(year: number, month: number): number {
  const ad = bsToAD(year, month, 1);
  return ad.getDay();
}

export function getFirstWeekdayOfADMonth(year: number, month: number): number {
  return new Date(year, month, 1).getDay();
}

export function getDaysInADMonth(year: number, month: number): number {
  return new Date(year, month + 1, 0).getDate();
}

export function todayBS(): { year: number; month: number; day: number } {
  const today = new Date();
  return adToBS(today.getFullYear(), today.getMonth(), today.getDate());
}
